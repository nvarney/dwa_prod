<h1>Chore lists with requirements and rewards </h1>

<h2>Description:</h2>
	I generally give my son a list of chores to complete on the weekends. This list consists of items like cleaning his room or vacuuming. It also includes the rewards he will receive if he completes these tasks by a certain time. I have found through experience that he does really well with these when the definition of “finished” is explicitly spelled out. This application will have in essence be a virtual list that I can create and update.

<h2>Features:</h2>
<ul>
	<li>
		Store common chores in a database<br>
		I will be able to create new chores and add their requirements to the database.<br>
	</li>
	<li>
		Generate a chore list<br>
		I can check off a list of existing chores to add to the current list<br>
		I can also set the time until completion and reward for the time<br>
	</li>
	<li>
		Update active chore list with completed tasks<br>
		My son will be able to click on tasks once they are finished to remove them from the list<br>
		When the list is empty the app displays what reward he can choose<br>
	</li>
</ul>

